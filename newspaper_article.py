class NewspaperArticle:
    def __init__(self, title, text, url, author, summary, published, updated,
                 category, keywords):
        self.title = title
        self.text = text
        self.url = url
        self.summary = summary
        self.author = author
        self.published = published
        self.update = updated
        self.category = category
        self.keywords = keywords